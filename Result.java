public class Result{
public static void main(String[]args){
int avgmarks=85;
if(avgmarks>=90){
System.out.println("Outstanding rank");
}
else if(avgmarks>=75){
System.out.println("Distinction");
}
else if(avgmarks>=60){
System.out.println("First Class");
}
else if(avgmarks>35){
System.out.println("Pass");
}
else
{
System.out.println("Fail");
}
}
}

